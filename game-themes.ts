export interface GameTheme {
  id: string;
  name: string;
  description: string;
  icon: string;
  status: 'Play Now' | 'Coming Soon';
  symbols: string[]; // Ordered from most common to rarest
  payouts: { [symbol: string]: { [count: number]: number } };
  bonusSymbol: string;
  freeSpinsRewards: { [count: number]: number };
}

// Helper to generate payouts based on symbol order
const generatePayouts = (symbols: string[]): { [symbol: string]: { [count: number]: number } } => {
  const payouts: { [symbol: string]: { [count: number]: number } } = {};
  const basePayouts = [2, 5, 8, 10, 15, 20, 30, 50, 75, 100, 250]; // Base values for 3-of-a-kind

  symbols.forEach((symbol, index) => {
    const base = basePayouts[index] || basePayouts[basePayouts.length - 1];
    payouts[symbol] = {
      3: base,
      4: base * 2,
      5: base * 5,
    };
  });
  return payouts;
};

const createTheme = (data: Omit<GameTheme, 'payouts' | 'freeSpinsRewards'> & { freeSpinsRewards?: { [count: number]: number } }): GameTheme => {
  return {
    ...data,
    payouts: generatePayouts(data.symbols),
    freeSpinsRewards: data.freeSpinsRewards || { 3: 5, 4: 10, 5: 25 },
  };
};

